    public PixelFont(Texture2D pixel)
    {
        _pixel = pixel;
    }

    public void Draw(SpriteBatch spriteBatch, string text, Vector2 position, Color color, int scale = 2)
    {
        int cursorX = (int)position.X;
        int cursorY = (int)position.Y;

        foreach (char sourceCharacter in text.ToUpperInvariant())
        {
            if (sourceCharacter == '\n')
            {
                cursorX = (int)position.X;
                cursorY += 9 * scale;
                continue;
            }

            string[] glyph = _glyphs.TryGetValue(sourceCharacter, out string[]? pattern) ? pattern : _glyphs['?'];
            for (int row = 0; row < glyph.Length; row++)
            {
                for (int column = 0; column < glyph[row].Length; column++)
                {
                    if (glyph[row][column] == '1')
                    {
                        spriteBatch.Draw(_pixel, new Rectangle(cursorX + column * scale, cursorY + row * scale, scale, scale), color);
                    }
                }
            }

            cursorX += 6 * scale;
        }
    }

    public int MeasureWidth(string text, int scale = 2)
    {
        return text.Length * 6 * scale;
    }
